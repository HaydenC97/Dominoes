def set_joints(joints):
	# publish the joint targets
	if real_robot == True:
		group.go(joint_goal, wait=True)
		group.stop() # makes sure no residual movements left
	else:
		for i in range(7):
			joint_publishers[i].publish(joints[i])
		rospy.sleep(pause)

def gripper_open():
	# publish the command to open the gripper
	if real_robot == True:
		# initialise the client
		client = actionlib.SimpleActionClient('/franka_gripper/move', MoveAction)
		client.wait_for_server()
		goal = MoveGoal(width = 0.08, speed = 0.04)
		client.send_goal(goal)
		client.wait_for_result(rospy.Duration.from_sec(5.0))
	else:
		# open coordinates = 0.06
		msg.data = [0.06, 0.06]
		gripper_publishers.publish(msg)
		rospy.sleep(pause)

def gripper_close():
	# publish the command to close the gripper
	if real_robot == True:
		# initialise the client
		# using grasp for a better grip of the brick
		client = actionlib.SimpleActionClient('/franka_gripper/grasp', GraspAction)
		rospy.loginfo("CONNECTING")
		client.wait_for_server()
		action = GraspGoal(width=0.5,speed=0.08,force=1)
		rospy.loginfo("SENDING ACTION")
		client.send_goal(action)
		client.wait_for_result(rospy.Duration.from_sec(5.0))
		rospy.loginfo("DONE")
	else:
		# close coordinates = 0
		msg.data = [0, 0]
		gripper_publishers.publish(msg)
		rospy.sleep(pause)

